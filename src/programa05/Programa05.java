package programa05;

/**
 * ++ incrementa 1
 * -- decrementa 1
 * += incrementa o valor
 * -= decrementa o valor
 */

public class Programa05 {

    public static void main(String[] args) {
        int x = 10;

        x++;
        System.out.println(x);

        x--;

        System.out.println(x);

        x += 5;
        System.out.println(x);

        x -= 5;
        System.out.println(x);

    }

}
