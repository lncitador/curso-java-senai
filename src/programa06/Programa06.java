package programa06;

/**
 * Operadores de comparação
 * == igual
 * != diferente
 * > maior
 * < menor
 * >= maior ou igual
 * <= menor ou igual
 */

public class Programa06 {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x >= y: " + (x >= y));
        System.out.println("x <= y: " + (x <= y));

    }
}
